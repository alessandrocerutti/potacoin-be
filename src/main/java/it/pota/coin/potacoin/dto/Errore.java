package it.pota.coin.potacoin.dto;

public class Errore {
	private String msg;
	private int id;
	
	public Errore() {
		
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
}
